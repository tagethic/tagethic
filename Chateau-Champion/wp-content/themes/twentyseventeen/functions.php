  $results = $wpdb->get_results("SELECT p.post_name my_name, p.guid my_guid, p.post_title my_title, p.post_date my_date, p.post_excerpt my_legende FROM $wpdb->posts p, $wpdb->term_relationships r, $wpdb->term_taxonomy tt, $wpdb->terms t WHERE p.post_mime_type = 'application/pdf' AND upper(p.post_title) like upper('$FindPostTitle') AND r.object_id = p.id AND tt.term_taxonomy_id = r.term_taxonomy_id AND tt.taxonomy = 'category_media' AND t.term_id = tt.term_id AND t.slug = '$cat' ORDER BY p.post_date DESC ", OBJECT);
  //
  // loop through results
  $output = "";
  if ( $results ) {
	$output = "<div class='pdflist'>";
	if($label!=="") {$output .= "<h3>".$label."</h3>";}
	$output .= "<ul>";
    foreach ( $results as $attachment ) {
		list($y, $s) = sscanf($attachment->my_date, "%d-%s");
		$l = ($attachment->my_legende!==""?"<span>".$attachment->my_legende."</span>" : "");
		$output .= "<li><a href='". $attachment->my_guid ."' rel='bookmark' title='". $attachment->my_title."' data-pub='". $y."' target='_blank'>". $attachment->my_title."</a> ". $l."</li>";
    }
	$output .= "</ul></div>";
  }
 return $output;
}
